#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import process from "node:process";

const ROOT_DIR = process.cwd();
const CONFIG_PATH = path.join(ROOT_DIR, "scripts", "mindmap.config.json");

const args = new Set(process.argv.slice(2));
const checkOnly = args.has("--check");
const silent = args.has("--silent");

function readConfig() {
  const raw = fs.readFileSync(CONFIG_PATH, "utf8");
  return JSON.parse(raw);
}

function toNodeId(input) {
  const normalized = input
    .replace(/[^a-zA-Z0-9/_-]/g, "_")
    .replace(/[/-]+/g, "_")
    .replace(/_+/g, "_")
    .replace(/^_+|_+$/g, "");
  const withFallback = normalized || "node";
  const startsWithLetter = /^[a-zA-Z_]/.test(withFallback)
    ? withFallback
    : `n_${withFallback}`;
  const reserved = new Set(["end", "subgraph", "graph", "flowchart", "mindmap"]);
  return reserved.has(startsWithLetter) ? `${startsWithLetter}Node` : startsWithLetter;
}

function escapeLabel(input) {
  return input.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function sortEntries(entries) {
  return entries.sort((a, b) => {
    if (a.isDirectory() !== b.isDirectory()) {
      return a.isDirectory() ? -1 : 1;
    }
    return a.name.localeCompare(b.name);
  });
}

function collectTree(config) {
  const ignoreDirs = new Set(config.ignoreDirs ?? []);
  const ignoreFiles = new Set(config.ignoreFiles ?? []);
  const outputFile = (config.outputFile ?? "").split("/").join(path.sep);

  const root = {
    id: "root",
    label: config.rootLabel ?? "Repository",
    children: []
  };

  function walk(currentRelPath, parentNode, depth) {
    const currentAbsPath = path.join(ROOT_DIR, currentRelPath);
    const entries = sortEntries(fs.readdirSync(currentAbsPath, { withFileTypes: true }));

    for (const entry of entries) {
      if (entry.name.startsWith(".DS_Store")) {
        continue;
      }

      const relPath = path.join(currentRelPath, entry.name);
      const relPathForId = relPath.split(path.sep).join("/");
      const nextDepth = depth + 1;
      if (relPath === outputFile) {
        continue;
      }

      if (entry.isDirectory()) {
        if (ignoreDirs.has(entry.name)) {
          continue;
        }
        if (nextDepth > config.maxDepth) {
          continue;
        }
        const node = {
          id: toNodeId(relPathForId),
          label: relPathForId,
          children: []
        };
        parentNode.children.push(node);
        walk(relPath, node, nextDepth);
      } else {
        if (ignoreFiles.has(entry.name)) {
          continue;
        }
        if (nextDepth > config.includeFilesAtDepth) {
          continue;
        }
        parentNode.children.push({
          id: toNodeId(relPathForId),
          label: relPathForId,
          children: []
        });
      }
    }
  }

  walk(".", root, 0);
  return { root };
}

function emitMindmapNode(lines, node, depth) {
  const indent = "  ".repeat(depth);
  if (depth === 1) {
    lines.push(`${indent}${node.id}(("${escapeLabel(node.label)}"))`);
  } else {
    lines.push(`${indent}${node.id}["${escapeLabel(node.label)}"]`);
  }
  for (const child of node.children) {
    emitMindmapNode(lines, child, depth + 1);
  }
}

function buildMermaid(graph) {
  const lines = [];
  lines.push("%% Auto-generated by scripts/generate-codebase-mindmap.mjs");
  lines.push("%% Do not edit this file manually.");
  lines.push("");
  lines.push("# Codebase Mindmap");
  lines.push("");
  lines.push("- Generated from repository structure using `pnpm map:generate`");
  lines.push("- Auto-update modes: `pnpm map:watch` and pre-commit hook");
  lines.push("");
  lines.push("```mermaid");
  lines.push("mindmap");
  emitMindmapNode(lines, graph.root, 1);

  lines.push("```");
  lines.push("");
  lines.push("## How to regenerate");
  lines.push("");
  lines.push("- One-shot: `pnpm map:generate`");
  lines.push("- Watch: `pnpm map:watch`");
  lines.push("- CI check: `pnpm map:check`");
  lines.push("");
  lines.push("## Source config");
  lines.push("");
  lines.push("- `scripts/mindmap.config.json`");
  lines.push("");
  return `${lines.join("\n")}`;
}

function run() {
  const config = readConfig();
  const graph = collectTree(config);
  const nextContent = buildMermaid(graph);
  const outputPath = path.join(ROOT_DIR, config.outputFile);
  const currentContent = fs.existsSync(outputPath)
    ? fs.readFileSync(outputPath, "utf8")
    : "";

  if (checkOnly) {
    if (currentContent !== nextContent) {
      console.error(`Mindmap is stale: ${config.outputFile}`);
      process.exitCode = 1;
      return;
    }
    if (!silent) {
      console.log(`Mindmap is up to date: ${config.outputFile}`);
    }
    return;
  }

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, nextContent, "utf8");
  if (!silent) {
    console.log(`Mindmap generated: ${config.outputFile}`);
  }
}

run();
